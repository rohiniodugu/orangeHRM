package jumpstaments;

public class breakandcontinueexample {

	public static void main(String[] args) {
		for(int i=2;i<=6;i++)
			
		
		{
			System.out.println(i+"selenium");
			
		
		
		if(i==3)
			{
			System.out.println(i+"webdriver");
			
			
          //  break;
            continue;
           // continue keyword will skip the current statement of the loop and 
           // will be continuing with the rest of the iterations (based on the condition)
            
            
			}
		System.out.println(i+"testing");
		System.out.println("laddu");
	}
		System.out.println("%%%%%%%%%%%%%%%%%");
		System.out.println();
		for(int i=2;i<=3;i++)
		{
			System.out.println(i+"selenium");
			if(i==3)
			{
				System.out.println(i+"webdriver");
			
			//break;
			
			continue;
			//continue keyword will skip the current  statement of the loop and
			//will be continuing with the rest of the iterations (based on  the condition)
			
		}
		System.out.println(i+"testing");
		System.out.println("laddu");
	}
	}
}