package com.loops;

public class Nestedloopsexample {
	public static void main(String[] args) {
		for(int i=1;i<=3;i++)
		{
		for(int j=1;j<=2;j++)
		{
			System.out.println("srini");
		}
		
		}

		System.out.println("@@@@@@@@2@@@@@@@@@@");
		System.out.println();
		for(int i=1;i<=3;i++)
		{
		for(int j=1;j<=2;j++)
		{
			System.out.println("*");
		}
		
		}

		System.out.println("@@@@@@@@3@@@@@@@@@@");
		System.out.println();
		for(int i=1;i<=3;i++)
		{
		for(int j=1;j<=2;j++)
		{
			System.out.println(i);
	
		}
		}
	    System.out.println("@@@@@@@@4@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
		System.out.println(j);
	    }
	    }
	    System.out.println("@@@@@@@@5@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
		System.out.println(i+j);
	    }
	    }
	    
	    System.out.println("@@@@@@@@6@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
		System.out.println("i"+"j");
	    }
	    }

	    
	    
	    System.out.println("@@@@@@@@7@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
		System.out.println(i+"_"+j);
	    }
	    }
	    System.out.println("@@@@@@@@8@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
    		System.out.println(i);
    		System.out.println();
	    }
	    }
	    System.out.println("@@@@@@@@9@@@@@@@@@@");
	    System.out.println();
	    for(int i=1;i<=3;i++)
	    {
        for(int j=1;j<=2;j++)
	    {
    		System.out.println("*");
    		System.out.println();
	    }
	    }
	}
}
		
    				
    				
    				
    				
    				
    				
    				
    
  
	
	
