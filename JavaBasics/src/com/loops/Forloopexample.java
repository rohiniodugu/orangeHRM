package com.loops;

public class Forloopexample {
public static void main(String[] args) {
	for(int var=1;var<=3;var=var+1)
	{
		System.out.println(var+""+"hello");
				
		
	}
	for(int test=2;test<=7;test=test+3)
	{
		System.out.println(test+""+"venkat");
	}
	for(int a=6;a>=-10;a--)
	{
		System.out.println(a+"rohini" );
		
	}
}
}
